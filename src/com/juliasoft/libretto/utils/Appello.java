package com.juliasoft.libretto.utils;

public class Appello {

	private String esame;
	private String data;
	private String anno;
	private String crediti;
	private String ora;
	private String tipo;
	private String modulo;
	private String docenti;
	private String link;

	public String getAnno() {
		return anno;
	}

	public String getCrediti() {
		return crediti;
	}

	public String getData() {
		return data;
	}

	public String getDocenti() {
		return docenti;
	}

	public String getEsame() {
		return esame;
	}

	public String getLink() {
		return link;
	}

	public String getModulo() {
		return modulo;
	}

	public String getOra() {
		return ora;
	}

	public String getTipo() {
		return tipo;
	}

	public void setAnno(String anno) {
		this.anno = anno;
	}

	public void setCrediti(String crediti) {
		this.crediti = crediti;
	}

	public void setData(String data) {
		this.data = data;
	}

	public void setDocenti(String docenti) {
		this.docenti = docenti;
	}

	public void setEsame(String esame) {
		this.esame = esame;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public void setModulo(String modulo) {
		this.modulo = modulo;
	}

	public void setOra(String ora) {
		this.ora = ora;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
}
